# What are the Data types and operators
# Boolean, Numbers and Strings
number_1 = 4
number_2 = 2

# Operators: Arithmetic Operators: - + * / %
print(number_1 + number_2)
print(number_1 - number_2)
print(number_1 * number_2)
print(number_1 / number_2)
print(number_1 % number_2)
print(number_1 ** number_2) # exponential

# Comparison Operators > <  == != >= <=
print(number_1 < number_2)
print(number_1 != number_2)
print(number_1 >= number_2)


